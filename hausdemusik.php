        <p><img src="images/base/title-design.jpg" width="165" height="24" alt="" /><br />Haus-de-musik　Aoyamaについて</p><br />
        
            ハウス・デ・ムジカのホームページにアクセスしていただき、ありがとうございます。<br />
「ハウス・デ・ムジカ」は「お家で音楽を！」のドイツ語を元に考えました。<br />
横浜との境にほど近いこの湘南鷹取に、1981年、私共は移り住んで参りました。<br />
          半年が過ぎた頃、ヤマハの講師だった私の所に、主人の知り合いの方の御紹介で、生徒さんが５名、レッスンに来てくださるようになりました。これが私のピアノ教室の始まりで大変感謝！！以来10～15名程の生徒さんとピアノ教室を続けさせていただいております。当時は、２年に１度、ヤマハの講師仲間とホールでの大きな発表会を行っておりましたが、「出番が短い曲１曲だけでは・・・？」という思いがあり、<br /><br />
          
          私共のデュオやアンサンブル、何か弾けるようになってからは息子や娘も参加。私の手作りケーキやクッキーのティータイムも賑やかで、子供達はもちろん、生徒さんもとても楽しそうにしてくださっていたのを覚えています。10年程前からは、横須賀・横浜界隈にも手軽に借りられるホールがいくつも完成したため、私の生徒さんだけでホールを借り、ホームコンサートのような発表会を行うことが出来るようになりました。でも、大きな舞台で演奏できる事は良い経験でしたが、お客様のほとんどいらっしゃらないホールはとても淋しいものでした。最近、家のまわりでもお散歩していらっしゃる方とよくお会いします。小学生ぐらいまでの小さなお子様やお母様も案外多く見受けられます。「遠くのホールまでは・・・・？？」とおっしゃる方にもちょっと立ち寄っていただいて、音楽の楽しいひとときを御一緒できたら・・・・といつの日からか考えるようになりました。<br /><br />

ある時は生徒さんの発表の場。ある時は娘や音楽仲間のミニコンサート。ある時は主人自慢のオーディオコンサート。室内楽の集いに始まって、小さな弦楽アンサンブルの会もこの湘南鷹取の我家で出来たら・・・・！！と夢が広がってきました。2008年にONGAKU NO OUCHIとして娘がホームページを作りはじめました。その後2009年にハウス・デ・ムジカ＠横須賀＆横浜として、娘が中心になって香川さんのご指導の下、ホームページを作りました。2010年には娘が久我山の私の実家でもバイオリン教室を始めたため、ハウス・デ・ムジカ＠横須賀/横浜＆東京と致しました。<br /><br />

この度、お隣の井上健一さんがホームページ作成の会社を立ち上げる準備をしていると伺いましたので、<br />
これまでの内容に幾つか項目を付け加えたホームページの全面的なリニューアルをお願いし、<br />
Haus-de-Musik-Aoyamaと致しました。<br />
皆様に温かく見守っていただけたら幸いです。また皆様からの御連絡を心よりお待ちいたしております。 <br /><br />
