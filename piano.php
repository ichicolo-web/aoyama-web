            	<li><span>【青山由貴子】</span></li>
                
                <li>東京杉並区に生まれる。4歳よりヤマハ音楽教室に通う。</li>
                <li>その後『窓ぎわのトットちゃん』で有名な小学校の校長先生のお嬢様である本間みさを先生にピアノを師事。</li>
