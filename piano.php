                <li>ヤマハ講師資格を取得。大学卒業と同時にヤマハ音楽教室幼児科ジュニア科のシステム講師となり、ピアノ教室も始める。</li>
                <li>また、高校時代より室内楽や合唱伴奏を始め、コールアミカル、ヴォーチェ・ムジカーレなど数々の合唱団の伴奏にあたり現在に至る。ピアノ講師歴33年。</li>
